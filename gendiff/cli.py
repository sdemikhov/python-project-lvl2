# -*- coding: utf-8 -*-

"""The cli module parse user entered arguments."""

import argparse


parser = argparse.ArgumentParser(description='Generate diff')

parser.add_argument(
    'first_file',
    help='location in a file system for first file'
    )
parser.add_argument(
    'second_file',
    help='location in a file system for second file'
    )
parser.add_argument(
    '-f',
    '--format',
    help=(
        "Output format: 'default', 'plain', 'json'"
    ),
    choices=['default', 'plain', 'json'],
    metavar='FORMAT',
    dest='output_format',
    default='default',
)
